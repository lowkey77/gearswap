-- Setup vars that are user-independent.  state.Buff vars initialized here will automatically be tracked.
function job_setup()
	
	-- Whether to use Luzaf's Ring
    state.LuzafRing = M(false, "Luzaf's Ring")
    -- Whether a warning has been given for low ammo
    state.warned = M(false)

	--List of which WS you plan to use TP bonus WS with.
	moonshade_ws = S{'Leaden Salute','Wildfire','Last Stand'}
	
	autows = 'Leaden Salute'
	autofood = 'Sublime Sushi'

    define_roll_values()
	update_combat_form()
	
	init_job_states({"Capacity","AutoTrustMode","AutoWSMode","AutoFoodMode","AutoShootMode","AutoStunMode","AutoDefenseMode","LuzafRing"},{"OffenseMode","RangedMode","WeaponskillMode","ElementalMode","IdleMode","TreasureMode",})
end


